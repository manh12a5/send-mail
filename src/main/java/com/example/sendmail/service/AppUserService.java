    @Autowired
    private AppUserRepository appUserRepository;

    @Autowired
    private ConfirmationTokenRepository confirmationTokenRepository;

    @Autowired
    private EmailSender emailSender;

    @Autowired
    private BCryptPasswordEncoder bCryptPasswordEncoder;

    private final static String USER_NOT_FOUND = "User with email %s not found";

    @Override
    public UserDetails loadUserByUsername(String email) throws UsernameNotFoundException {
        return appUserRepository.findByEmail(email)
                .orElseThrow(() -> new UsernameNotFoundException(String.format(USER_NOT_FOUND, email)));
    }

    @Transactional
    public String signupUser(AppUser appUser, HttpServletRequest request) {
        boolean isAppUser = appUserRepository.findByEmail(appUser.getEmail()).isPresent();
        if (isAppUser) {
            throw new IllegalStateException("Email already taken");
        }
        String encodePassword = bCryptPasswordEncoder.encode(appUser.getPassword());
        appUser.setPassword(encodePassword);

        appUserRepository.save(appUser);

        String token = UUID.randomUUID().toString();
        ConfirmationToken confirmationToken = new ConfirmationToken(
                token,
                LocalDateTime.now(),
                LocalDateTime.now().plusMinutes(10),
                appUser
        );
        confirmationTokenRepository.save(confirmationToken);

        String url = applicationUrl(token, request);
        Map<String, String> templateAttributes = new LinkedHashMap<>();
        templateAttributes.put("name", appUser.getLastName());
        templateAttributes.put("url", url);

        emailSender.send(appUser.getEmail(), templateAttributes);

        log.info("Confirmation Token is: {}", token);
        return "Saved user";
    }

    public void enableAppUser(String email) {
        AppUser appUser = appUserRepository.findByEmail(email)
                .orElseThrow(() ->new UsernameNotFoundException(String.format(USER_NOT_FOUND, email)));
        appUser.setEnabled(true);
        appUserRepository.save(appUser);
    }

    private String applicationUrl(String token, HttpServletRequest request) {
        return "http://"
                + request.getServerName() //localhost
                + ":"
                + request.getServerPort() //Port
                + request.getRequestURI() // (/api/v1/registration) requestURI = contextPath + servletPath + pathInfo
                + "/confirm?token=" + token;
    }

